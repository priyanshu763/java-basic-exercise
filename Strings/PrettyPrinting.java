package Strings;

public class PrettyPrinting {
    public static void main(String[] args){
        float a = 453.1274f;
        System.out.printf("Formatted numbrer is %.2f",a);
        //returns the value of float up to 2 decimal value after rounding off.

        System.out.printf("pie: %.3f",Math.PI);
        //returns the value of pi up to 3 decimal value after rounding off

        System.out.printf("hello my name is %s and I am %s", "Priyanshu", "cool");





    }
}